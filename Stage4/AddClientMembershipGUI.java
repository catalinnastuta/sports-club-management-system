    private ClientFile readClientFile(String filename) {
        ClientFile clientFile = new ClientFile();
        try (BufferedReader br = new BufferedReader(new FileReader(filename))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(delimiter);
                if (data.length == 3) {
                    String name = data[0];
                    String email = data[1];
                    String phone = data[2];
                    Client client = new Client(name, email, phone);
                    clientFile.addClient(client);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading file");
        }
        return clientFile;
    }
    
    private void updateCSV() {
        try {
            File file = new File("memberships.csv");
            BufferedWriter bf = new BufferedWriter(new FileWriter(file));
            
            for (Membership membership : membershipFile.getAllMemberships().values()) {
                String name = membership.getClient().getName();
                String tier = membership.getTier().toString();
                
                String row = name + "," + tier + "\n";
                
                bf.write(row);
            }
            
            bf.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error writing file");
        }
    }

