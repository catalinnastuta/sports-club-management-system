    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();

        Client client = clientFile.getClients(name);
        if (client != null){
            jButton3.setEnabled(true);

            jTextField2.setText(client.getName());
            jTextField3.setText(client.getEmail());
            jTextField4.setText(client.getPhone());

            // Check if the client has a membership by reading memberships.csv
            Membership membership = checkMembershipFromFile(name);
            if (membership != null) {
                JOptionPane.showMessageDialog(this, "This client already has a membership.");
                jTextField5.setText(membership.getTier().toString());
            } else {
                jTextField5.setText(""); // Clear the membership field if no membership is found
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Client does not exist in records");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String membershipInfo = jTextField5.getText();
        String name = jTextField2.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please search for a client first!");
            return;
        }
    
        Client client = clientFile.getClients(name);
    
        if (client == null) {
            JOptionPane.showMessageDialog(this, "Client not found!");
            return;
        }

        Membership.Tier tier = Membership.Tier.valueOf(membershipInfo.toUpperCase());
        Membership membership = new Membership(client, tier);
        membershipFile.addMembership(membership);
        updateCSV();
        JOptionPane.showMessageDialog(this, "Membership added successfully!"); 
    }//GEN-LAST:event_jButton2ActionPerformed

