    private Membership checkMembershipFromFile(String clientName) {
    try (BufferedReader reader = new BufferedReader(new FileReader("memberships.csv"))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] data = line.split(",");
            if (data.length >= 2 && data[0].trim().equalsIgnoreCase(clientName)) {
                // Membership found
                Membership.Tier tier = Membership.Tier.valueOf(data[1].trim().toUpperCase());
                return new Membership(clientFile.getClients(clientName), tier);
            }
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
    return null; // No membership found
}
    
