    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String searchName = jTextField1.getText().trim();
        if (searchName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a client name to search.");
            return;
        }

        String clientsCsvFilePath = "clients.csv"; // Default file path for clients
        String membershipsCsvFilePath = "memberships.csv"; // Default file path for memberships
        try (BufferedReader clientsReader = new BufferedReader(new FileReader(clientsCsvFilePath));
            BufferedReader membershipsReader = new BufferedReader(new FileReader(membershipsCsvFilePath))) {

            String line;
            while ((line = clientsReader.readLine()) != null) {
                String[] rowData = line.split(",");
                if (rowData.length >= 3 && rowData[0].equalsIgnoreCase(searchName)) {
                    jTextField2.setText(rowData[0]); // Set name
                    jTextField3.setText(rowData[2]); // Set email
                    jTextField4.setText(rowData[1]); // Set phone

                // Read memberships data and set membership
                    while ((line = membershipsReader.readLine()) != null) {
                        String[] membershipData = line.split(",");
                        if (membershipData.length >= 2 && membershipData[0].equalsIgnoreCase(searchName)) {
                            jTextField5.setText(membershipData[1]); // Set membership
                            break; // Found the membership, no need to continue searching
                        }
                    }
                    return; // Client found, exit the method
                }
            }

        // If client not found
            JOptionPane.showMessageDialog(this, "Client not found.");
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nameToRemove = jTextField2.getText().trim();
        if (nameToRemove.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No client to remove.");
            return;
        }

        // Remove client from clients.csv and memberships.csv
        removeClientFromFiles(nameToRemove);

        // Clear fields after removal
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");

        JOptionPane.showMessageDialog(this, "Client removed successfully.");
    }//GEN-LAST:event_jButton2ActionPerformed

