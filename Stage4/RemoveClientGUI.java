    private void removeClientFromFile(String clientsFilePath, String membershipsFilePath, String nameToRemove) {
        try (BufferedReader clientsReader = new BufferedReader(new FileReader(clientsFilePath));
            BufferedReader membershipsReader = new BufferedReader(new FileReader(membershipsFilePath));
            BufferedWriter clientsWriter = new BufferedWriter(new FileWriter(clientsFilePath + ".tmp"));
            BufferedWriter membershipsWriter = new BufferedWriter(new FileWriter(membershipsFilePath + ".tmp"))) {

            // Remove client from clients.csv
            String line;
            while ((line = clientsReader.readLine()) != null) {
                String[] rowData = line.split(",");
                if (rowData.length >= 3 && rowData[0].equalsIgnoreCase(nameToRemove)) {
                    // Skip this line as it matches the client to be removed
                    continue;
                }
                clientsWriter.write(line);
                clientsWriter.newLine();
            }

            // Remove client from memberships.csv
            while ((line = membershipsReader.readLine()) != null) {
                String[] rowData = line.split(",");
                if (rowData.length >= 2 && rowData[0].equalsIgnoreCase(nameToRemove)) {
                    // Skip this line as it matches the client to be removed
                    continue;
                }
                membershipsWriter.write(line);
                membershipsWriter.newLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        // Rename the temporary files to the original files
        try {
            Files.move(Paths.get(clientsFilePath + ".tmp"), Paths.get(clientsFilePath), StandardCopyOption.REPLACE_EXISTING);
            Files.move(Paths.get(membershipsFilePath + ".tmp"), Paths.get(membershipsFilePath), StandardCopyOption.REPLACE_EXISTING);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    private void removeClientFromFiles(String nameToRemove) {
        String clientsCsvFilePath = "clients.csv"; // Default file path for clients
        String membershipsCsvFilePath = "memberships.csv"; // Default file path for memberships
        removeClientFromFile(clientsCsvFilePath, membershipsCsvFilePath, nameToRemove);
    }

